package com.hemliv.vo;

public class Condition {
	private String proName;
	private String proFormat;
	private String proSid;
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getProFormat() {
		return proFormat;
	}
	public void setProFormat(String proFormat) {
		this.proFormat = proFormat;
	}
	public String getProSid() {
		return proSid;
	}
	public void setProSid(String proSid) {
		this.proSid = proSid;
	}
	
}
